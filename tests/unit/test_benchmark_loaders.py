def test_load_refresh_stability_shape_and_acceptance_target() -> None:
    base, extended, metadata = load_refresh_stability()

    assert len(base) == 128
    assert len(extended) == 132
    assert len(extended) - len(base) == 4
    assert metadata["stable"] is True
    assert metadata["acceptance_target"]["minimum_non_unexplained_fraction"] == 0.9
    assert metadata["date_column"] in base.columns
    assert metadata["target_column"] in base.columns
    assert set(metadata["spend_columns"]).issubset(base.columns)
    assert set(metadata["spend_columns"]).issubset(extended.columns)


def test_refresh_stability_extended_is_base_plus_four_weeks() -> None:
    """The first 128 rows of the extended dataset must be bit-equal to the
    base. Without this, a refresh fit sees regenerated noise in the
    historical window and conflates it with the four genuinely new weeks —
    which would corrupt the NFR-5 movement-classification benchmark.
    """
    base, extended, _ = load_refresh_stability()
    head = extended.iloc[: len(base)].reset_index(drop=True)
    assert base.equals(head)


