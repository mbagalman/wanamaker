"""Unit tests for benchmark dataset loaders."""

from __future__ import annotations

import pytest

from wanamaker.benchmarks.loaders import (
    load_public_example,
    load_public_example_metadata,
    load_refresh_stability,
    load_synthetic_ground_truth,
)


def test_load_synthetic_ground_truth() -> None:
    data, truth = load_synthetic_ground_truth()

    assert len(data) == truth["n_weeks"] == 150
    assert truth["date_column"] in data.columns
    assert truth["target_column"] in data.columns
    assert len(truth["spend_columns"]) == 12
    assert set(truth["spend_columns"]).issubset(data.columns)
    assert set(truth["control_columns"]).issubset(data.columns)
    assert len(truth["channels"]) == 12
    assert len(truth["weekly_contributions"]) == 12
    assert len(truth["top_3_channels"]) == 3
    assert truth["total_media_contribution"] > 0


def test_synthetic_ground_truth_channel_totals_match_weekly_contributions() -> None:
    _, truth = load_synthetic_ground_truth()

    weekly = truth["weekly_contributions"]
    totals = {channel["name"]: channel["total_contribution"] for channel in truth["channels"]}
    for channel, values in weekly.items():
        assert sum(values) == pytest.approx(totals[channel])


def test_load_public_example_shape_and_metadata() -> None:
    df = load_public_example()
    metadata = load_public_example_metadata()

    assert len(df) == 156
    assert metadata["n_weeks"] == 156
    assert metadata["source"] == "Generated by benchmark_data/generate_phase1_benchmarks.py"
    assert metadata["date_column"] in df.columns
    assert metadata["target_column"] in df.columns
    assert set(metadata["spend_columns"]).issubset(df.columns)
    assert 6 <= len(metadata["spend_columns"]) <= 10
    assert len(metadata["control_columns"]) >= 1


def test_load_refresh_stability_shape_and_acceptance_target() -> None:
    base, extended, metadata = load_refresh_stability()

    assert len(base) == 128
    assert len(extended) == 132
    assert len(extended) - len(base) == 4
    assert metadata["stable"] is True
    assert metadata["acceptance_target"]["minimum_non_unexplained_fraction"] == 0.9
    assert metadata["date_column"] in base.columns
    assert metadata["target_column"] in base.columns
    assert set(metadata["spend_columns"]).issubset(base.columns)
    assert set(metadata["spend_columns"]).issubset(extended.columns)


def test_refresh_stability_extended_is_base_plus_four_weeks() -> None:
    """The first 128 rows of the extended dataset must be bit-equal to the
    base. Without this, a refresh fit sees regenerated noise in the
    historical window and conflates it with the four genuinely new weeks —
    which would corrupt the NFR-5 movement-classification benchmark.
    """
    base, extended, _ = load_refresh_stability()
    head = extended.iloc[: len(base)].reset_index(drop=True)
    assert base.equals(head)
