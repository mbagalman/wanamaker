from wanamaker.benchmarks.loaders import (
    load_public_example,
    load_public_example_metadata,
    load_refresh_stability,
    load_synthetic_ground_truth,
)


def test_load_synthetic_ground_truth():
    df, gt = load_synthetic_ground_truth()
    assert df is not None
    assert gt is not None
    assert "channels" in gt


def test_load_public_example():
    df = load_public_example()
    metadata = load_public_example_metadata()

    assert len(df) == 156
    assert metadata["n_weeks"] == 156
    assert metadata["source"] == "Generated by benchmark_data/generate_phase1_benchmarks.py"
    assert metadata["date_column"] in df.columns
    assert metadata["target_column"] in df.columns
    assert set(metadata["spend_columns"]).issubset(df.columns)
    assert 6 <= len(metadata["spend_columns"]) <= 10
    assert len(metadata["control_columns"]) >= 1


def test_load_refresh_stability():
    base, extended, metadata = load_refresh_stability()

    assert len(base) == 128
    assert len(extended) == 132
    assert len(extended) - len(base) == 4
    assert metadata["stable"] is True
    assert metadata["acceptance_target"]["minimum_non_unexplained_fraction"] == 0.9
    assert metadata["date_column"] in base.columns
    assert metadata["target_column"] in base.columns
    assert set(metadata["spend_columns"]).issubset(base.columns)
    assert set(metadata["spend_columns"]).issubset(extended.columns)
    assert extended[metadata["date_column"]].iloc[: len(base)].equals(base[metadata["date_column"]])
