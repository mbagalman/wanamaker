"""Loader functions for the eight named benchmark datasets (NFR-7).

The datasets themselves are generated/curated in Phase 0 and committed to
``benchmark_data/`` at the repo root. These loaders return them as
DataFrames (or, for synthetic ground truth, as DataFrame plus the known
underlying contributions).
"""

from __future__ import annotations

import json
from pathlib import Path

import pandas as pd

BENCHMARK_DIR = Path(__file__).resolve().parents[3] / "benchmark_data"


def load_public_example() -> pd.DataFrame:
    """Return the public quickstart example dataset.

    The dataset is generated by ``benchmark_data/generate_phase1_benchmarks.py``.
    It is synthetic, anonymized, and safe to commit publicly.

    Raises:
        FileNotFoundError: If the generated CSV is missing.
    """
    data_path = BENCHMARK_DIR / "public_example.csv"
    if not data_path.exists():
        raise FileNotFoundError(
            f"public example CSV not found at {data_path}. "
            "Run benchmark_data/generate_phase1_benchmarks.py."
        )
    return pd.read_csv(data_path, parse_dates=["week"])


def load_public_example_metadata() -> dict:
    """Return metadata and ground-truth recipe for the public example dataset."""
    metadata_path = BENCHMARK_DIR / "public_example_metadata.json"
    if not metadata_path.exists():
        raise FileNotFoundError(
            f"public example metadata not found at {metadata_path}. "
            "Run benchmark_data/generate_phase1_benchmarks.py."
        )
    return json.loads(metadata_path.read_text(encoding="utf-8"))


def load_refresh_stability() -> tuple[pd.DataFrame, pd.DataFrame, dict]:
    """Return the refresh-stability base data, extended data, and metadata.

    The base and extended files are generated from the same deterministic
    process. The extended version contains exactly four additional weeks and
    is the benchmark input for NFR-5 refresh movement classification.

    Raises:
        FileNotFoundError: If any generated refresh benchmark artifact is missing.
    """
    base_path = BENCHMARK_DIR / "refresh_stability_base.csv"
    extended_path = BENCHMARK_DIR / "refresh_stability_4_weeks_added.csv"
    metadata_path = BENCHMARK_DIR / "refresh_stability_metadata.json"
    missing = [
        str(path)
        for path in (base_path, extended_path, metadata_path)
        if not path.exists()
    ]
    if missing:
        raise FileNotFoundError(
            "refresh-stability benchmark artifacts are missing: "
            f"{missing}. Run benchmark_data/generate_phase1_benchmarks.py."
        )
    base = pd.read_csv(base_path, parse_dates=["week"])
    extended = pd.read_csv(extended_path, parse_dates=["week"])
    metadata = json.loads(metadata_path.read_text(encoding="utf-8"))
    return base, extended, metadata


def load_synthetic_ground_truth() -> tuple[pd.DataFrame, dict]:
    """Return (data, ground_truth_contributions)."""
    data_path = BENCHMARK_DIR / "synthetic_ground_truth.csv"
    truth_path = BENCHMARK_DIR / "synthetic_ground_truth_ground_truth.json"
    if not data_path.exists():
        raise FileNotFoundError(
            f"synthetic ground-truth CSV not found at {data_path}. "
            "Run benchmark_data/generate_synthetic_ground_truth.py."
        )
    if not truth_path.exists():
        raise FileNotFoundError(
            f"synthetic ground-truth metadata not found at {truth_path}. "
            "Run benchmark_data/generate_synthetic_ground_truth.py."
        )
    data = pd.read_csv(data_path, parse_dates=["week"])
    truth = json.loads(truth_path.read_text(encoding="utf-8"))
    return data, truth


def load_low_variation_channel() -> tuple[pd.DataFrame, dict]:
    """Return the low-variation-channel diagnostic dataset and its metadata.

    One spend column has CV well below 0.10, exercising
    ``check_spend_variation`` (FR-2.2) and the spend-invariant saturation
    handling (FR-3.2).
    """
    return _load_diagnostic_dataset("low_variation_channel")


def load_collinearity() -> tuple[pd.DataFrame, dict]:
    """Return the collinearity diagnostic dataset and its metadata.

    Two paid channels are constructed with absolute correlation > 0.95 so
    ``check_collinearity`` (FR-2.2) fires on the pair.
    """
    return _load_diagnostic_dataset("collinearity")


def load_lift_test_calibration() -> tuple[pd.DataFrame, pd.DataFrame, dict]:
    """Return the lift-test calibration dataset, its lift-test CSV, and metadata.

    Used to validate FR-1.3 (lift-test prior wiring) and FR-3.3 (recovered
    ROI within the supplied 95 % CI). One channel has a known true ROI
    that the companion lift-test CSV reports.

    Returns:
        ``(data, lift_tests, metadata)``.
    """
    data, metadata = _load_diagnostic_dataset(
        "lift_test_calibration", parse_dates=["week"]
    )
    lift_tests_path = BENCHMARK_DIR / "lift_test_calibration_lift_tests.csv"
    if not lift_tests_path.exists():
        raise FileNotFoundError(
            f"lift-test CSV not found at {lift_tests_path}. "
            "Run benchmark_data/generate_diagnostic_benchmarks.py."
        )
    lift_tests = pd.read_csv(lift_tests_path, parse_dates=["test_start", "test_end"])
    return data, lift_tests, metadata


def load_target_leakage() -> tuple[pd.DataFrame, dict]:
    """Return the target-leakage diagnostic dataset and its metadata.

    A control column is constructed as a near-perfect linear transform of
    the target so ``check_target_leakage`` (FR-2.2) fires.
    """
    return _load_diagnostic_dataset("target_leakage")


def load_structural_break() -> tuple[pd.DataFrame, dict]:
    """Return the structural-break diagnostic dataset and its metadata.

    A deliberate step change in baseline at a known week index gives
    ``check_structural_breaks`` (FR-2.2) a clear signal to detect.
    """
    return _load_diagnostic_dataset("structural_break")


def _load_diagnostic_dataset(
    name: str,
    *,
    parse_dates: list[str] | None = None,
) -> tuple[pd.DataFrame, dict]:
    """Shared loader for the diagnostic benchmark datasets (issue #25).

    Each dataset is a single CSV (``<name>.csv``) plus a metadata JSON
    (``<name>_metadata.json``). Both are produced by
    ``benchmark_data/generate_diagnostic_benchmarks.py``.
    """
    data_path = BENCHMARK_DIR / f"{name}.csv"
    metadata_path = BENCHMARK_DIR / f"{name}_metadata.json"
    missing = [str(p) for p in (data_path, metadata_path) if not p.exists()]
    if missing:
        raise FileNotFoundError(
            f"{name} benchmark artifacts are missing: {missing}. "
            "Run benchmark_data/generate_diagnostic_benchmarks.py."
        )
    data = pd.read_csv(data_path, parse_dates=parse_dates or ["week"])
    metadata = json.loads(metadata_path.read_text(encoding="utf-8"))
    return data, metadata
